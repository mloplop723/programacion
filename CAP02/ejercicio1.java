

public class ejercicio1 {
    public static void main(String[] args) {
        int x = 144;
        int a = 999;
        
        System.out.println("x = " + x);
        System.out.println("a = " + a);
        System.out.println("x + a = " + (x + a));
        System.out.println("x - a = " + (x - a));
        System.out.println("x / a = " + ((float)x / (float)a));
        System.out.println("x * a = " + (x * a));
      }
}
